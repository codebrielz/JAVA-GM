public class Variables {
    public static void main(String args[]) {
        System.out.println("--Variable en JAVA--");
        System.out.println("");
        // Tipos primitivos
        System.out.println("--Tipos primitivos--");

        System.out.println("Tenemos dos tipos primitivos que son:");
        System.out.println("Tipos Enteros:");
        int miVariableEntera = 30;
        System.out.println("Mi variable entera: " + miVariableEntera);
        System.out.println("Tipos Flotantes:");
        float miVariableFloat = 3.05f;
        // Modificamos el valor de la variable
        miVariableFloat = 3.15f;
        System.out.println("Mi variable float: " + miVariableFloat);
        System.out.println("");

        System.out.println("--Tipos referenciados--");
        System.out.println("Clases");
        System.out.println("Interfaces");
        System.out.println("Arreglos");
    }
}
