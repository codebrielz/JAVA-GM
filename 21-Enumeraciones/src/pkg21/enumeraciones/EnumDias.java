
package pkg21.enumeraciones;

/*
    Primera diferencia entre una clase y una enumeracion es el tipo del metodo que tenemos que indicar, pasa de class a enum.
    Los elementos escritos en un metodo enum por default son public static final.
    Es decir, son una lista de valores que son constantes.
    Por default, son strings.
*/
public enum EnumDias {
    LUNES,
    MARTES,
    MIERCOLES,
    JUEVES,
    VIERNES,
    SABADO,
    DOMINGO
}
